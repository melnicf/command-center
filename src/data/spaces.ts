// Helper function to generate logo URLs using Logo.dev (official Clearbit replacement)
// Logo.dev requires an API token. If no token is provided, returns undefined
// so the component can use its fallback (colored box with app name)
function getLogoUrl(domain: string): string | undefined {
  // You can set LOGO_DEV_TOKEN in your .env.local file
  // Get your free token at https://logo.dev
  const token = process.env.NEXT_PUBLIC_LOGO_DEV_TOKEN;
  
  if (!token) {
    // No token provided - return undefined to use component fallback
    return undefined;
  }
  
  // Logo.dev format: https://img.logo.dev/{domain}?token={token}&size=128
  return `https://img.logo.dev/${domain}?token=${token}&size=128&format=png`;
}

